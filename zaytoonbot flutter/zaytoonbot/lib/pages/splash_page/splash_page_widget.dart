                              0.0, 50.0, 0.0, 0.0),
                          child: FFButtonWidget(
                            onPressed: () async {
                              await actions.turnOnBluetooth();

                              context.pushNamed(
                                'BluetoothDeviceListPage',
                                queryParameters: {
                                  'isBTEnabled': serializeParam(
                                    true,
                                    ParamType.bool,
